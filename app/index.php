    function updateWaktu() {
      const bulan = ["Jan", "Feb", "Mar", "Apr", "Mei", "Jun", "Jul", "Agu", "Sep", "Okt", "Nov", "Des"];
      const now = new Date();

      const tgl  = now.getDate().toString().padStart(2, '0');
      const bln  = bulan[now.getMonth()];
      const thn  = now.getFullYear();

      const jam    = now.getHours().toString().padStart(2, '0');
      const menit  = now.getMinutes().toString().padStart(2, '0');

      document.getElementById("tanggal").textContent = `${tgl}.${bln}.${thn}`;
      document.getElementById("waktu").textContent = `${jam}:${menit}`;
    }

    setInterval(updateWaktu, 1000);
    updateWaktu();// Jalankan sekali saat load
</script>
